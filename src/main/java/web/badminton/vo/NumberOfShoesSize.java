package web.badminton.vo;

public class NumberOfShoesSize {
	private int idShoesSize;
	private int idProduct;
	private int idTypeShoesSize;
	private int numberSize;
	public int getIdShoesSize() {
		return idShoesSize;
	}
	public void setIdShoesSize(int idShoesSize) {
		this.idShoesSize = idShoesSize;
	}
	public int getIdProduct() {
		return idProduct;
	}
	public void setIdProduct(int idProduct) {
		this.idProduct = idProduct;
	}
	public int getIdTypeShoesSize() {
		return idTypeShoesSize;
	}
	public void setIdTypeShoesSize(int idTypeShoesSize) {
		this.idTypeShoesSize = idTypeShoesSize;
	}
	public int getNumberSize() {
		return numberSize;
	}
	public void setNumberSize(int numberSize) {
		this.numberSize = numberSize;
	}
	
	
}
